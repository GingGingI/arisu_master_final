public class LocationData {

    private double lat;
    private double lon;

    public void LocationData(){}

    public void setLat(double lat){
        this.lat = lat;
    }
    public void setLon(double lon){
        this.lon = lon;
    }
    public double getLat(){
        return lat;
    }
    public double getLon(){
        return lon;
    }
}
